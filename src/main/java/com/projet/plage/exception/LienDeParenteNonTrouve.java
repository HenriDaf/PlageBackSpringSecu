package com.projet.plage.exception;

public class LienDeParenteNonTrouve extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public LienDeParenteNonTrouve() {
		super();

	}

	public LienDeParenteNonTrouve(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public LienDeParenteNonTrouve(String message, Throwable cause) {
		super(message, cause);

	}

	public LienDeParenteNonTrouve(String message) {
		super(message);

	}

	public LienDeParenteNonTrouve(Throwable cause) {
		super(cause);

	}
	
	

}
